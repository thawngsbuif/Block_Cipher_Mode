static const unsigned char Rcon[11] = {
    0x00,0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80,0x1B,0x36
};

static void aes_key_expansion(const unsigned char *key, unsigned char *round_keys)
{
    // round_keys: (AES_NR + 1) * 16 byte
    memcpy(round_keys, key, 16);

    unsigned char temp[4];
    int i = AES_NK;

    while (i < AES_NB * (AES_NR + 1)) {
        temp[0] = round_keys[4*(i-1) + 0];
        temp[1] = round_keys[4*(i-1) + 1];
        temp[2] = round_keys[4*(i-1) + 2];
        temp[3] = round_keys[4*(i-1) + 3];

        if (i % AES_NK == 0) {
            // RotWord
            unsigned char t = temp[0];
            temp[0] = temp[1];
            temp[1] = temp[2];
            temp[2] = temp[3];
            temp[3] = t;
            // SubWord
            temp[0] = sbox[(int)temp[0]];
            temp[1] = sbox[(int)temp[1]];
            temp[2] = sbox[(int)temp[2]];
            temp[3] = sbox[(int)temp[3]];
            // XOR Rcon
            temp[0] ^= Rcon[i / AES_NK];
        }

        round_keys[4*i + 0] = round_keys[4*(i-AES_NK) + 0] ^ temp[0];
        round_keys[4*i + 1] = round_keys[4*(i-AES_NK) + 1] ^ temp[1];
        round_keys[4*i + 2] = round_keys[4*(i-AES_NK) + 2] ^ temp[2];
        round_keys[4*i + 3] = round_keys[4*(i-AES_NK) + 3] ^ temp[3];

        i++;
    }
}

static void aes_add_round_key(unsigned char state[16], const unsigned char *key)
{
    for (int i = 0; i < 16; i++) {
        state[i] ^= key[i];
    }
}

static void aes_sub_bytes(unsigned char state[16])
{
    for (int i = 0; i < 16; i++) {
        state[i] = sbox[(int)state[i]];
    }
}

static void aes_inv_sub_bytes(unsigned char state[16])
{
    for (int i = 0; i < 16; i++) {
        state[i] = rsbox[(int)state[i]];
    }
}

static void aes_shift_rows(unsigned char state[16])
{
    unsigned char tmp[16];

    tmp[0]  = state[0];
    tmp[1]  = state[5];
    tmp[2]  = state[10];
    tmp[3]  = state[15];

    tmp[4]  = state[4];
    tmp[5]  = state[9];
    tmp[6]  = state[14];
    tmp[7]  = state[3];

    tmp[8]  = state[8];
    tmp[9]  = state[13];
    tmp[10] = state[2];
    tmp[11] = state[7];

    tmp[12] = state[12];
    tmp[13] = state[1];
    tmp[14] = state[6];
    tmp[15] = state[11];

    memcpy(state, tmp, 16);
}

